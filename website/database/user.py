class User:
  def __init__(self, first_name, second_name, birth_date, birth_month, birth_year, permissions, password,
                    participant_in_studies, own_researches, creation_date, creation_month, creation_year):
    self.first_name = first_name
    self.second_name = second_name
    self.birth_date = birth_date
    self.birth_month = birth_month
    self.birth_year = birth_year
    self.permissions = permissions
    self.password = password
    self.participant_in_studies = participant_in_studies
    self.own_researches = own_researches
    self.creation_date = creation_date
    self.creation_month, creation_month
    self.creation_year, creation_year
